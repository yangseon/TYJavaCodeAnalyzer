package com.tyn.jca;

public class LocInfo {
	
	private long loc = 0;
	
	private long codeLoc = 0;
	
	public LocInfo() {
		loc = 0;
		codeLoc = 0;
	}

	public long getLoc() {
		return loc;
	}

	public void setLoc(long loc) {
		this.loc = loc;
	}

	public long getCodeLoc() {
		return codeLoc;
	}

	public void setCodeLoc(long codeLoc) {
		this.codeLoc = codeLoc;
	}

	
}
